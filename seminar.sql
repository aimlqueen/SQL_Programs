SELECT CAST(23 AS Decimal(4,2));

-- 1. EXTRACT
SELECT EXTRACT(YEAR FROM NOW()) AS Current_Year;
SELECT EXTRACT(MONTH FROM '2023-04-03');
SELECT EXTRACT(HOUR FROM '15:30:00');

-- 2. DAY
SELECT DAY('2024-10-05') AS Day;

-- 3. HOUR
SELECT HOUR('15:30:00') AS Hour;

-- 4. LAST_DAY
SELECT LAST_DAY('2024-10-05') AS Last_Day_of_Month;

-- 5. LOCALTIME
SELECT LOCALTIME();-- Expecting 'HH:MM:SS'

-- 6. LOCALTIMESTAMP
SELECT LOCALTIMESTAMP();  -- Expecting 'YYYY-MM-DD HH:MM:SS'

SELECT LOCALTIMESTAMP,LOCALTIME;

-- 7. MAKEDATE
SELECT MAKEDATE(2024, 100) AS Date_From_Year_and_Day;

-- 8. MAKETIME
SELECT MAKETIME(12, 30, 45) AS Time_From_Hour_Minute_Second;

-- 9. MICROSECOND
SELECT MICROSECOND('12:30:45.123456') AS Microsecond;

-- 10. MINUTE
SELECT MINUTE('12:30:45') AS Minute;

-- 11. MONTH
SELECT MONTH('2024-10-05') AS Month;

-- 12. MONTHNAME
SELECT MONTHNAME('2024-10-05') AS Month_Name;

-- 13. NOW
SELECT NOW() AS Current_Date_and_Time;

SELECT sysdate(),now();

-- 14. PERIOD_ADD
SELECT PERIOD_ADD(202411, 2) AS Period_Add;

-- 15. PERIOD_DIFF
SELECT PERIOD_DIFF(202412, 202410) AS Period_Difference;

-- 16. QUARTER
SELECT QUARTER('2024-10-05') AS Quarter;

-- 17. SECOND
SELECT SECOND('12:30:45') AS Second;

-- 18. STR_TO_DATE
SELECT STR_TO_DATE('2024-10-05', '%Y-%m-%d') AS Converted_Date;
SELECT STR_TO_DATE('2024/10/05 14:30:45', '%Y/%d/%m %H:%i:%s') AS Converted_DateTime;

-- 19. SUBDATE
SELECT SUBDATE('2024-10-05', INTERVAL 10 DAY) AS Date_Subtracted;

-- 20. SUBTIME
SELECT SUBTIME('12:30:00', '01:00:00') AS Time_Subtracted;

-- 21. SYSDATE
SELECT SYSDATE() AS System_Date_and_Time;

-- 22. TIMEDIFF
SELECT TIMEDIFF('15:00:00', '12:30:00') AS Time_Difference;

-- 23. WEEKOFYEAR
SELECT WEEKOFYEAR('2024-10-05') AS Week_of_Year;
